    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        try {
            if(txt_maPhong.getText().trim().equals("")||txt_giaPhong.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mã phòng, giá phòng không được để trống");
                return;
            }
            int dem=0;
            Iterator<Phong> it = lstPhong.iterator();
            while (it.hasNext()) {
                Phong next = it.next();
                if(next.getMaPhong().trim().equals(txt_maPhong.getText().trim())){
                    dem++;
                }
            }
            if(dem==0){
                lstPhong.removeAll(lstPhong);
                qlPhongDao.insertPhong(txt_maPhong.getText().trim(), cbb_loaiPhong.getSelectedItem().toString(),Double.parseDouble(txt_giaPhong.getText().trim()),cbb_TinhTrang.getSelectedItem().toString(),txta_ghiChu.getText().trim());
                loadData();
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            }
            else{
                JOptionPane.showMessageDialog(null, "Mã phòng đã tồn tại");
                //loadData();
                return;
            }
            xoaTrang();
        }
        catch (Exception ex) {
           JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_btn_themActionPerformed

