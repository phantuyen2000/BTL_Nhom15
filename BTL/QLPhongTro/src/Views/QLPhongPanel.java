    private void btn_TimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimActionPerformed
        // TODO add your handling code here:
        try {
            ArrayList<Phong> lstCanTim=new ArrayList<>();
            // TODO add your handling code here
            if(txt_maPhongTC.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Vui lòng nhập mã phòng cần tìm");
                return;
            }
            int dem=0;
            Iterator<Phong> it = lstPhong.iterator();
            while (it.hasNext()) {
                Phong next = it.next();
                if(next.getMaPhong().trim().equals(txt_maPhongTC.getText().trim())){
                    dem++;
                }
            }
            if(dem>0){
                    lstCanTim=qlPhongDao.findPhong(txt_maPhongTC.getText().trim());
                    tb_Phong.setModel(new CustomPhong(lstCanTim));
                    JOptionPane.showMessageDialog(null, "Có tìm thấy thông tin về phòng này"); 
            }
            else{
                JOptionPane.showMessageDialog(null, "Không tìm thấy phòng có mã này");
            }
            xoaTrang();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        txt_maPhongTC.setText("");
    }//GEN-LAST:event_btn_TimActionPerformed

