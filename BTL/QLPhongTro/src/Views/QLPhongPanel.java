    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            if(txt_maPhong.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Vui lòng nhập mã phòng cần xóa hoặc click vào 1 dòng để xóa");
                return;
            }
            int dem=0;
            Iterator<Phong> it = lstPhong.iterator();
            while (it.hasNext()) {
                Phong next = it.next();
                if(next.getMaPhong().trim().equals(txt_maPhong.getText().trim())){
                    dem++;
                }
            }
            if(dem>0){
                 lstPhong.removeAll(lstPhong);
                if(JOptionPane.showConfirmDialog(this, "Xác nhận", "Xóa sinh viên này", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                qlPhongDao.detelePhong(txt_maPhong.getText().trim());
                loadData();
                JOptionPane.showMessageDialog(null, "Xóa thành công");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Mã phòng cần xóa không tồn tại");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        xoaTrang();
    }//GEN-LAST:event_btn_xoaActionPerformed

